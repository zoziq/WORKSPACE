package fipa;

import java.io.Serializable;

/**
 * This interface is used to use the Composite design pattern in Expression
 * structure structure as described in <a
 * href="http://www.fipa.org/specs/fipa00070/">FIPA ACL Message Representation
 * in String Specification</a>
 * 
 * @author Aegeant-S Group Aug 13, 2003
 */
public interface IExpressionElement extends Serializable {
	/**
	 * Returns the encoded string of this IExpressionElement
	 * 
	 * @return encoded string of this IExpressionElement
	 */
	abstract public String getEncodedStr();

	/**
	 * Sets the encoded string of this IExpressionElement
	 * 
	 * @param encodedStr
	 *            the encoded string of this IExpressionElement
	 */
	abstract public void setEncodedStr(String encodedStr);
}